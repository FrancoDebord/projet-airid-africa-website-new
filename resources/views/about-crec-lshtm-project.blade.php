        <h4 class="title-section">About</h4>

        <p class="text-justify">
            The CREC-LSHTM Collaborative Research Programme is a research partnership set up in 2003, between the London
            School of Hygiene & Tropical Medicine in the UK and the Centre de Recherche Entomologique de Cotonou held at
            the Ministry of Health in Benin. The partnership has extensive experience in the evaluation of vector
            control products (mainly long-lasting insecticidal nets and Indoor residual spraying) to WHO standards. It
            is part of PAMVERC (The Pan-African Malaria Vector Research Consortium) and its research activities are
            largely funded by the Bill & Melinda Gates Foundation (BMGF) through the Innovative Vector Control
            Consortium (IVCC),
            the WHO and chemical companies to a lesser extent.
        </p>
        <p class="text-justify mt-2">
            We test malaria vector control products such as Long Lasting Insecticide Nets and insecticides for Indoor
            Residual Spray developed by chemical companies We perform both laboratory assays and field trials.
        </p>
    </div>


    <div class="col-12 col-md-6">
        <h4 class="title-section">Mission & Vision of the CREC/LSHTM Project</h4>

        <p class="text-justify">
            The CREC/LSHTM Collaborative Research Programme is a vector control product testing facility with a mission
            to provide the highest quality data on the efficacy of insecticidal products for use against vectors of
            human disease in line with guidelines set out by the World Health Organisation (WHO). The Facility aims to
            combat vector-borne diseases through the identification of a new generation of efficacious vector control
            products. CREC/LSHTM Facility is striving to be a world leader in generating accurate and reliable data on
            the efficacy of vector control products in WHO evaluation studies.
        </p>
    </div>
