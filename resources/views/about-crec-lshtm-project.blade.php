        <h4 class="title-section">The content of CREC/LSHTM Collaborative Research Programme </h4>

        <p class="text-justify mt-2">
            We work with organisations by testing their products (LLINs and/or IRS) to assess their efficacy and impact
            on vector populations. We also design methods and experiments to test products. Phase 1 testing is performed
            at the Bioassay Laboratory of the main Facility available at Akpakpa, Cotonou-Benin. Phase 2 testing is done
            in experimental huts at our field site in Cov√®, Central Benin.
        </p>

        <p class="text-justify mt-2">
            <strong>In terms of services, we provide : </strong>

        <ul class="">
            <li>
                Laboratory evaluation of insecticide-treated products (i.e. long lasting insecticide treated nets),
                insecticide treated clothing (i.e. blankets, wall hangings, plastic sheeting), Indoor Residual Spray
                (IRS), new insecticides, formulations, larvicides etc.

            </li>
            <li>
                Field evaluation of insecticide treated products (i.e. long lasting insecticide treated nets),
                insecticide treated clothing (i.e. blankets, wall hangings, plastic sheeting), IRS, new insecticides,
                formulations, larvicides and repellent

            </li>
            <li>
                Evaluation of novel product types which may involve modification of standard methods as well as the
                development of new experimental procedures

            </li>
        </ul>
