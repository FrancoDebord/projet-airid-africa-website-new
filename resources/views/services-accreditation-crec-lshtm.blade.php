    <div class="col-12 col-md-6 mt-2">
        <h5 class="title-section">Randomised controlled trials</h5>

        <p class="text-justify mt-2">
            Building on from our experience with the New Nets Project, CREC/LSHTM is well-equiped to perform randomised
            controlled trials of vector control products with clinical outcomes. We have access to communities which are
            suitable for evaluating the epidemiological impact of LLINS, IRS, spatial repellents and larvicides and
            other vector control products.

            <br>
            Our team is equiped with epidemiologists, nurses, sociologists, community engagement experts and
            entomologists with adequate experience in performing large scale epidemiological trials of vector control
            products.
