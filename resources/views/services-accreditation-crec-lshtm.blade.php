    <div class="col-12 col-md-6 mt-2">
        <h5 class="title-section">WHOPES phase 1 testing</h5>

        <p class="text-justify mt-2">
            <strong>We perform the following Phase1 studies in our facility: </strong>

        <ul>
            <li>WHO Cone bioassays on insecticide treated net samples for assessing the efficacy of insecticide treated
                nets against laboratory-maintained mosquito colonies under controlled laboratory conditions</li>

            <li>WHO Cone bioassays on IRS treated block substrates for assessing of efficacy IRS insecticides against
                laboratory-maintained colonies of mosquitoes under controlled laboratory conditions</li>

            <li>Tunnel Test, a simulator assay performed on insecticide treated net samples to assess the efficacy of
                insecticide treated nets on the behaviour of laboratory-maintained colonies of mosquitoes under
                controlled laboratory conditions</li>

            <li>Cube Tests, a simulator assay for assessing the efficacy of IRS products against laboratory-maintained
                colonies of mosquitoes under controlled laboratory conditions</li>

            <li>Cylinder Test for investigating/monitoring resistance in test mosquito populations/colonies CDC Bottle
                Bioassays for investigating/monitoring resistance to insecticidal products in test mosquitoes</li>


        </ul>
        </p>
    </div>

    <div class="col-12 col-md-6 mt-2">
        <h5 class="title-section">WHOPES phase 3 testing</h5>

        <p class="text-justify mt-2">
            <strong>We perform the following Phase 3 studies in our facility: </strong>

        <ul>
            <li>Large-scale field trials to establish the efficacy of insecticide formulations at the selected
                application rates against the target vector species, when applied to all or most households in the
                community;</li>

            <li>Monitoring of mosquito resting density in treated rooms by human landing catches or CDC light trap
                catches for measuring the efficacy and residual activity of the insecticide treatment;</li>

            <li>Measure the parous rate of a sample of mosquitoes collected in HLC or space spray catches to estimate
                mosquito lifespan in the field;</li>

            <li>Diagnosis of Plasmodium species using an ELISA test to detect circum-sporozoite protein (CSP) of
                Plasmodium falciparum or P. vivax in the crushed heads and thoraxes of specimens, which are stored dry
                on silica gel;</li>

            <li>Bioassays on treated surfaces or treated nets using WHO plastic cones to determine insecticide
                bioavailability and residual activity.</li>
        </ul>
        </p>
    </div>

    <div class="col-12 col-md-6 mt-2">
        <h5 class="title-section">WHOPES phase 2 testing</h5>

        <p class="text-justify mt-2">
            <strong>We perform the following Phase 2 studies in our facility: </strong>

        <ul>

            <li>Experimental hut trials for assessing the efficacy of IRS insecticides, insecticide treated nets and
                other indoor vector control products against wild free-flying mosquitoes under semi-field conditions
            </li>


            <li>Cone bioassays for assessing the effectiveness of IRS treatments on sprayed walls</li>
        </ul>
        </p>
